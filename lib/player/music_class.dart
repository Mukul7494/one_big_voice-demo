class MusicList {
  final String title;
  final String artist;
  final String? album;
  final String id;
  final String artUri;
  final String displaySubtitle;
  final String audioUrl;

  MusicList({
    required this.title,
    required this.artist,
    required this.album,
    required this.id,
    required this.artUri,
    required this.displaySubtitle,
    required this.audioUrl,
  });
}
